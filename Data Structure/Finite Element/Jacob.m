function R=Jacob(v,x,y)
% v=[  v11  v12;
%         v21 v22;
%         v31 v32;
%         v41 v42];

R(1,1)=v(1,1)*(y-1)+v(2,1)*(1-y)+v(3,1)*y+v(4,1)*-y;
R(1,2)=v(1,1)*(x-1)+v(2,1)*(-x)+v(3,1)*x+v(4,1)*(1-x);
R(2,1)=v(1,2)*(y-1)+v(2,2)*(1-y)+v(3,2)*y+v(4,2)*-y;
R(2,2)=v(1,2)*(x-1)+v(2,2)*(-x)+v(3,2)*x+v(4,2)*(1-x);
 
 

